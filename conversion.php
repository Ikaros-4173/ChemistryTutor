  <p align="justify">4- Se multiplicarán las cantidades a convertir por los otros factores (Tanto Numeradores como Denominadores).</p>
  <p align="justify">5- Se dividen los resultados dados en el paso anterior.</p>
  <p align="justify">6- Y por último, se eliminan las unidades, quedando solamente las deseadas.</p>
