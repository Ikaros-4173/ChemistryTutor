<div id=”texto″><h1>Introducción a la química</h1>
  <br>
  <img src="matraces.jpg" border="0" width="400" height="200">
  <div class="box-grey"> Todos los cuerpos materiales tangibles o intangibles que nos rodean: aire, agua, ropa, pintura, papel, alimentos, bebidas gaseosas, juguetes, la generación de energía (eléctrica, luminosa, calorífica, etc), están relacionados directamente con la ciencia química, ya que esta sirve de base o fundamentos a las ciencias de la vida: biología y la física.
  <br>
  <br>
  La quimica interviene casi en todos los aspectos de nuestra vida: cultura y entorno (social y ambiental), por lo tanto, es erróneo pensar que la química es meramente teórica, y solo tiene que ver con formulas y nombres complicados de compuestos; cuando respiramos, digerimos los alimentos, nos lavamos con jabón, nos limpiamos los dientes con cierta pasta dental, cocinamos los alimentos, etc., estamos practicando química.</div>

  <h2 align="left">Concepto de química</h2>
  <br>
    <div class="box-grey">Es una ciencia natural basada en la observación y experimentación relacionado con los fenómenos que sufre la materia, de tal modo que de ella se estudie su composición, constitución, propiedades físicas y químicas, transformaciones y leyes que gobiernan dichos cambios en su estructura interna, así como la energía involucrada en el proceso.</div>

  <h2 align="left">Importancia de la química</h2>
  <br>
    <div class="box-grey">La quimica es una ciencia central, porque sirve de apoyo a otras ciencias como la física, la biología, la geología, la petroquímica, etc. Además permite satisfacer las necesidades humanas en diferentes áreas o campos de la actividad humana. Tenemos:
      <br><br>

      1) En medicina: La química ayuda con la síntesis de diferentes fármacos (antibióticos, analgésicos, antidepresivos, vacunas, vitaminas, hormonas, radioisótopos, etc), para el tratamiento de muchas enfermedades y para el mejoramiento de la salud en general.
      <br>
      2) En nutrición: La química permite sintetizar sustancias llamadas saborizantes y colorantes para mejorar ciertas propiedades de los alimentos, y de ese modo puedan ingerirse con facilidad; los preservantes para que los alimentos no se deterioren en corto tiempo; también la química determina las sustancias vitales que requiere el organismo (minerales, vitaminas, proteínas, etc)
    <br>
    3) En agricultura: Gracias a los productos químicos como abonos y fertilizantes se aumenta la productividad del suelo, y se logra satisfacer las necesidades de alimentación cada vez mas crecientes. Además con el uso de insecticidas, fungicidas y pesticidas, se controla muchas enfermedades y plagas que afectan al cultivo.
    <br>
    4) En textilería y cuidado de la ropa: La química ayuda potencialmente a satisfacer esta necesidad, Sintetizando muchas fibras textiles (rayón, orlón, nylon), colorantes para el teñido, sustancias para el lavado (jabones, detergentes, etc.), preservantes de fibras naturales y sintéticas, etc.
    <br
    >5) En medio ambiente: Ayuda en el tratamiento y control de sustancias contaminantes que afectan a nuestro ecosistema (agua, suelo y aire), y en la asistencia de desastres ecológicos tales como derrames de petróleo, caída de lluvia ácida, incendios forestales, etc.
    <br>
    6) En arqueología: Determinar antigüedad de restos fósiles.
    <br>
    7) En mineralogía: Técnicas de extracción y purificación de metales.
    <br>
    8) En astronomía: Combustibles químicos para los cohetes, ropa y alimentos concentrados para los astronautas.
  </div>
