<h4 align="justify">Cuando trabajan con números muy grandes o muy pequeños, los científicos, matemáticos e ingenieros usan notación científica para expresar esas cantidades. La notación científica es una abreviación matemática, basada en la idea de que es más fácil leer un exponente que contar muchos ceros en un número. Números muy grandes o muy pequeños necesitan menos espacio cuando son escritos en notación científica porque los valores de posición están expresados como potencias de 10. Cálculos con números largos son más fáciles de hacer cuando se usa notación científica.</h4>
